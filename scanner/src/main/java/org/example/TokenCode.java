package org.example;

/*
Class defining codes of tokens
 */

public enum TokenCode{
    INT,         // integer
    ID,           // ID (e.g. x,y,z)
    PLUS,         // +
    MINUS,        // -
    MUL,          // *
    DIV,          // /
    LPAREN,       // (
    RPAREN,       // )
    EOF,
    //  colors
    YELLOW,
    GREEN,
    BLUE,
    RED,
    WHITE
}